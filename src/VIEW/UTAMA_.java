    public void kode_nota() {//fungsi untuk menambahkan no nota otomatis
        list = dao.getNoNota();
        String[] dat = new String[1];
        int id = 0;
        int j = 0;
        for (entitas li : list) {
            dat[j] = li.getId_nota() + "";
            j++;
        }
        int g = 0;
        if (dat[g] == "") {
            id = 0;// apabila masih kosong di database
        } else {
            id = Integer.parseInt(dat[g]);
        }

        String kode_transaksi = ++id + "";
        kode_nota_txt.setText(kode_transaksi);
    }

    public int refresh() {// mendapatkan seluruh data menu
        list = dao.getMenu();
        String[][] data = new String[list.size()][3];
        int i = 0;
        int result = 0;
        for (entitas li : list) {
            data[i][0] = li.getKode_menu();
            data[i][1] = li.getNama_menu();
            data[i][2] = li.getHarga_menu() + "";
            i++;
        }
        String coba = String.valueOf(result);
        tabel_menu.setModel(new DefaultTableModel(data, new String[]{
            "Kode Menu", "Menu", "Harga"}));
        return result;
    }

