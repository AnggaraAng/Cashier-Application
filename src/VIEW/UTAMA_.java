    private void PrintActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_PrintActionPerformed
        try {
            // masuk data nota
            entitas e = new entitas();
            e.setId_nota(Integer.parseInt(kode_nota_txt.getText()));
            e.setTotal(totalSeluruh);
            e.setId_karyawan(Integer.parseInt(id_admin_txt.getText()));
            dao.inputNota(e);

            File file = new File("D://nota");
            if (!file.exists()) {
                file.mkdir();
            }
            FileWriter fw = new FileWriter("D://nota//" + kode_nota_txt.getText() + ".txt");
            BufferedWriter bw = new BufferedWriter(fw);
            bw.write("No Transaksi : " + kode_nota_txt.getText());
            bw.newLine();
            bw.write("_________________________________");
            bw.newLine();

            String header = String.format("%-16s %-16s %-16s %-16s", "Menu", "Harga",
                    "Jumlah", "Total");
            bw.write(header);
            bw.newLine();

            entitas ee = new entitas();
            for (int i = 0; i < tmp_kode.size(); i++) {
                String data = String.format("%-15s %-15s %-15s %-15s",
                        tmp_menu.get(i), tmp_harga.get(i),
                        tmp_jumlah.get(i), tmp_total.get(i));
                bw.write(data);

                bw.newLine();
                // masuk data transaksi

                ee.setId_nota(Integer.parseInt(kode_nota_txt.getText()));
                ee.setKode_menu(tmp_kode.get(i));
                ee.setJumlah(tmp_jumlah.get(i));
                dao.inputTransaksi(ee);
            }

            bw.write("Total : " + totalSeluruh);
            bw.newLine();
            bw.write("Kembali : " + refund);
            JOptionPane.showMessageDialog(this, "Transaksi Berhasil!", "Info", JOptionPane.INFORMATION_MESSAGE);
            init();
            kode_nota();

            bw.close();
        } catch (Exception e) {
        }
    }//GEN-LAST:event_PrintActionPerformed

