    private void caritxtKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_caritxtKeyReleased
        // TODO add your handling code here:
        list = dao.cariMenu(caritxt.getText());
        String[][] tmp = new String[list.size()][3];
        int i = 0;
        for (entitas c : list) {
            tmp[i][0] = c.getKode_menu();
            tmp[i][1] = c.getNama_menu();
            tmp[i][2] = c.getHarga_menu() + "";
            i++;
        }

        tabel_menu.setModel(new DefaultTableModel(tmp, new String[]{"Kode Menu", "Menu", "Harga"}));
    }//GEN-LAST:event_caritxtKeyReleased

